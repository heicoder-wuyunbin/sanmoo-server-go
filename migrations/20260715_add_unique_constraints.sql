-- ============================================================
-- Sanmoo Blog 数据完整性加固 - 添加缺失的唯一约束
-- ============================================================
-- 日期: 2026-07-15
-- 说明: 为 t_article_content 和 t_article_tag_rel 添加唯一约束，
--       防止数据重复，提升查询性能和数据完整性。
-- ============================================================

SET NAMES utf8mb4 COLLATE utf8mb4_unicode_ci;

-- ------------------------------------------------------------
-- 1. t_article_content 添加 article_id 唯一约束
-- ------------------------------------------------------------
-- 说明: 确保每篇文章只有一条内容记录，防止数据异常
-- ------------------------------------------------------------

ALTER TABLE `t_article_content`
  DROP INDEX `idx_article_id`,
  ADD UNIQUE KEY `uk_article_id` (`article_id`) USING BTREE;

-- ------------------------------------------------------------
-- 2. t_article_tag_rel 添加联合唯一约束
-- ------------------------------------------------------------
-- 说明: 确保同一篇文章不会重复关联同一个标签
-- ------------------------------------------------------------

ALTER TABLE `t_article_tag_rel`
  ADD UNIQUE KEY `uk_article_tag` (`article_id`, `tag_id`) USING BTREE;

-- ============================================================
-- 迁移脚本执行完成
